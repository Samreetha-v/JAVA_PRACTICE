package example.oop.encapsulation;

public class Encapsulate {

	private  int num;
	private String name;
	public int getInt() {
		return num;
	}
	public void setInt(int numb) {
		this.num=numb;
	}
	public String getName() {
		return name;
	}
	public void setName(String newname) {
		name=newname;
	}

}

